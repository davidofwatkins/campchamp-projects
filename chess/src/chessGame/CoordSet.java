package chessGame;

import java.io.Serializable;

public class CoordSet implements Serializable {
	
	private static final long serialVersionUID = 7674069701658869325L;
	private int yCoord;
	private int xCoord;
	
	public CoordSet(int y, int x) {
		yCoord = y;
		xCoord = x;
	}
	
	public void setCoords(int y, int x) {
		yCoord = y;
		xCoord = x;
	}
	
	public int getRow() {
		return yCoord;
	}
	
	public int getColumn() {
		return xCoord;
	}
}
